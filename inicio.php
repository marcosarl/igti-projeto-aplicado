A versão atual classifica os acordãos em:
<br><br>
BAN - Bancário<br>
CON - Contratos<br>
DAN - Dano, Responsabilidade Civil<br>
EXP - Expurgos Cumprimento<br>
OIG - OI Conhecimento<br>
OIE - OI Execução de Sentença<br>
SEG - Seguros em geral<br>
